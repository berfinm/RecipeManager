           public void showRecipes(){
    model.setRowCount(0);
    
        ArrayList<Recipes> recipes = new ArrayList<Recipes>();
        
        recipes = processes.getRecipes();
        
        if(recipes != null){
        
            for(Recipes recipe : recipes){
            
                Object[] next = {recipe.getId(),recipe.getAd(),recipe.getKategori(),recipe.getSure(),recipe.getTalimatlar()};
                
                model.addRow(next);
                
            }
            
            
            
        
        
        }
    
    
    
    
    
    }
    
    
    
