        public void showIngredients(){
    model.setRowCount(0);
    
        ArrayList<Ingredients> ingredients = new ArrayList<Ingredients>();
        
        ingredients = processes.getIngredients();
        
        if(ingredients != null){
        
            for(Ingredients ingredient : ingredients){
            
                Object[] next = {ingredient.getAd()};
                
                model.addRow(next);
                
            }
     
        }
    }
    
    
