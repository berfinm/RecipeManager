    private void tarifekleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tarifekleButtonActionPerformed
        // TODO add your handling code here:
        
        String ad = tarifismiTF.getText();
        String kategori = (String) tarifkategorisiCB.getSelectedItem();
        int sure = Integer.parseInt(tarifhazirlamasuresiTF.getText()); 
        String talimatlar = tarifinhazirlanisiTA.getText();
        String fotoUrl = "/Users/kagan/Desktop/projeyemek/"+ad.toLowerCase()+".jpg";
        
        if(processes.isRecipeExists(ad)){
            uyarilabel.setVisible(true);
        
        }else{
              processes.AddRecipes(ad, kategori, sure, talimatlar,fotoUrl);
              
              int i =0;
              while(i<ingredientIDlist.size()){
                              processes.AddRecipeIngredients(processes.GetRecipeDetails(ad).getId(),ingredientIDlist.get(i) ,ingredientMiktarlist.get(i));
              }

        dispose();
        
        }
        
        
    }//GEN-LAST:event_tarifekleButtonActionPerformed

