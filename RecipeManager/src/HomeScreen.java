        class CustomRenderer extends DefaultTableCellRenderer {
        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);

            // İlk sütundaki değeri al
            String firstColumnValue = table.getValueAt(row, 1).toString();

            // Koşula göre tüm satırın yazı rengini değiştir
            if (firstColumnValue.equals("0.0")) { // Koşulu kendine göre ayarla
                c.setForeground(Color.GREEN.darker());  // Yazı rengini yeşil yap
            } else {
                c.setForeground(Color.RED);    // Yazı rengini kırmızı yap
            }

            return c;
        }
    }
    
    

    

