    public void showRecipes(){      //model e tüm tarifleri gönderdiğimiz kısım
    model.setRowCount(0);
    
        ArrayList<Recipes> recipes = new ArrayList<Recipes>();
        recipes = processes.getRecipes();
        
        if(recipes != null){
            for(Recipes recipe : recipes){
                Object[] next = {recipe.getAd(),recipe.getSure(),recipe.getMaliyet()};
                model.addRow(next);
            }
        }
    }
    
    public void showIngredients(){      //model2 ye tüm malzemeleri gönderdiğimiz kısım
    model2.setRowCount(0);
    
        ArrayList<Ingredients> ingredients = new ArrayList<Ingredients>();
        ingredients = processes.getIngredients();
        
        if(ingredients != null){
            for(Ingredients ingredient : ingredients){
                Object[] next = {ingredient.getAd()};
                model2.addRow(next);
            }
        }
    }
            
            
    public void showIngredientSearchResult(){
            
            model3.setRowCount(0);
            
            ArrayList<Recipes> recipes = new ArrayList<Recipes>();
            
            for(Integer recipe:recipeList){
            
                recipes.add(processes.GetRecipeDetailsWithID(recipe));
            
            }
            
            
       if(recipes != null){
        
            for(Recipes recipe : recipes){
            
                Object[] next = {recipe.getAd()};
                
                model3.addRow(next);
                
            }
        }
            
            
            
    }
    
    
    public void showNameSearchResult(){     //Sonuç kısmını yazdırdığımız kısım
        model3.setRowCount(0);
    
       if(sendrecipeList.isEmpty()){
         ArrayList<Recipes> recipes = new ArrayList<Recipes>();
         recipes = processes.SearchWithName(aramaTF.getText(),
                                           siralamaCB.getSelectedItem().toString(), 
                                           malzemesayisiCB.getSelectedItem().toString(),
                                           maliyetaraligiCB.getSelectedItem().toString(),
                                           kategoriCB.getSelectedItem().toString());
        
         if(recipes != null){
             for(Recipes recipe : recipes){
                 Object[] next = {recipe.getAd(), processes.getEksikMalzemeMaliyeti(recipe.getAd())};
                 model3.addRow(next);
            }
        }
    
    
    
            }else{
        
                sendrecipeList = processes.SearchWithIngredient(sendrecipeList,
                                           siralamaCB.getSelectedItem().toString(), 
                                           malzemesayisiCB.getSelectedItem().toString(),
                                           maliyetaraligiCB.getSelectedItem().toString(),
                                           kategoriCB.getSelectedItem().toString());
          
          
                 if(sendrecipeList != null){
                      for(Recipes recipe: sendrecipeList){
                          recipe.setUyum(processes.calculateMatchingPercentage(recipe, ingredientlist));
                          Object[] next = {recipe.getAd(), processes.getEksikMalzemeMaliyeti(recipe.getAd()), recipe.getUyum()};
                          model3.addRow(next);
              }
          }
       }
  }
 
    private void startRecipeCheckTimer() {
        Timer timer = new Timer();
        timer.schedule(new TimerTask() {
            @Override
            public void run() {
                // Tariflerin boş olup olmadığını kontrol et
                if (aramaTF.getText().equals("")) {
                          aramaTF.setText("Tarif adı girerek arama yap.");
                          aramaTF.setForeground(Color.LIGHT_GRAY);
                } 
            }
        }, 0, 15000); // 0 gecikme, 10 saniyede bir kontrol
    }
    


